		<h4>Přestup do Barcelony</h4>
		<p>V lednu 2014 se objevily informace, že Barcelona mohla za Neymarův přestup zaplatit až 95 milionů eur (místo oficiálně zveřejněných 57 milionů), což by byl nejdražší transfer v historii fotbalu. Případ řešil španělský soud, který obdržel žalobu od zaměstnance Barcelony na klubového prezidenta Sandro Rosella za netransparentnost přestupu a zpronevěru. Rosell krátce nato odstoupil z funkce. Barcelona nakonec se svolením Neymarova otce Neymara Silvy Santose (který je i jeho agentem) zveřejnila účty ohledně přestupu Neymara. Brazilský klub Santos FC podle nich dostal z 57 milionů eur částku 17,1 milionu eur, zbytek dostala společnost, kterou vlastní hráčovi rodiče. Smlouva obsahuje i klazuli o vyplacení dalších 2 milionů eur, pokud se Neymar dostane během pěti let mezi finalisty Zlatého míče. Neymarův plat činí 56,7 milionu eur (za 5 let platnosti smlouvy).</p><br>

		<h3>Paris Saint-Germain FC</h3>
		<p>3. srpna 2017 byl potvrzen již nějakou dobu avizovaný přestup brazilského útočníka do francouzského klubu Paris Saint-Germain vlastněného bohatými katarskými majiteli. PSG využil vysoké klauzule ve smlouvě hráče, která činila 222 milionů eur (více než 6 miliard Kč k datu). Tento transfer překonal více než dvojnásobně hodnotu do té doby nejdražšího hráče, Francouze Paula Pogby, jenž přestoupil v srpnu 2016 z Juventusu do Manchesteru United za 110 milionů eur.</p><br><br>

