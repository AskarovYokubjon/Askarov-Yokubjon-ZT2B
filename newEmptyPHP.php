		<h1 style="color: rgb(10, 20, 60); text-align: center; ">NJR11</h1><hr style="border-width: 0.3px; border-color: rgb(10, 20, 60)">
		<p>Neymar da Silva Santos Júnior, či krátce Neymar. Je <i>Brazilský fotbalista</i>. Hraje jako útočník ve francouzském ligovém klubu <i>Paris Saint-Germain FC</i> a v brazilském národním týmu. Je považován za jednoho z nejtalentovanějších fotbalistů nejen v Brazílii, ale i ve světě. Vyniká vytříbenou technikou a rychlostí. Účastník <i>mistrovství světa 2014</i> a <i>2018</i>. <br><br>

		Dne 29. prosince 2010 obsadil třetí místo za Andrésem D'Alessandrem a <i>Juanem Sebastiánem Verónem</i> v anketě <i>Fotbalista roku Jižní Ameriky</i>, v roce 2011 a 2012 již tuto anketu ovládl. Za rok 2011 vyhrál cenu FIFA Puskás Award o nejhezčí gól roku. Je známý svým zrychlením, rychlostí, kličkováním, zakončením a technickými schopnostmi. Díky svému stylu hraní je kritiky, médii a fanoušky srovnáván s <i>Lionelem Messim</i> a brazilskou legendou Pelém, který sám označil Neymara za "nejlepšího fotbalistu na světě".</p><br>

		<h2 class="h2">Klubová kariéra</h2>
		<h3>Začátky</h3>
		<p>Narodil se v Mogi Das Cruzes v São Paulu. V roce 1992 se s rodinou přestěhoval do Sao Vicente, pak v roce 2003 do Santosu, kde začal hrát v témže roce fotbal za Santos FC. V sedmnácti podepsal plnou profesionální smlouvu se Santos FC.</p><br>

		<h3>Santos FC</h3>
		<p>Za Santos FC debutoval dne 7. března 2009 ve věku 17 let. Následující týden vstřelil svůj první gól za Santos proti Mogi Mirim. O měsíc později, 11. dubna, vstřelil gól proti Palmeiras na Campeonato Paulista a rozhodl tak o postupu do semifinále. Ve finále však Santos utrpěl porážku 4-2. Ve své první sezóně dal 14 gólů v 48 zápasech. Stal se nejlepším hráčem v roce 2010 a také nejlepším střelcem v roce 2010. <br><br>

        Neymar vstřelil celkem 6 gólů na Copa Libertadores 2011 a byl tak třetí nejlepší střelec. Ve finále proti uruguayskému týmu CA Peñarol dal jeden gól a Santos zvítězil 2:1. Výhra přinesla Santosu jejich první triumf v Poháru osvoboditelů od roku 1963, kdy hrála za klub brazilská legenda Pelé. <br><br>

		Dne 5. února 2012 vstřelil svůj stý gól jako profesionální fotbalista v zápase na Campeonato Paulista proti Palmeiras. Dne 7. března 2012 byl nejlepší hráč proti Internacionalu, kde Santos vyhrál 3-1. Trenér Mano Menezes řekl, že věří, že Neymar získá větší respekt a že je důležité, aby šel hrát do Evropy. 26. května 2013 Neymar oznámil, že podepíše pětiletou smlouvu se španělským velkoklubem FC Barcelona, přičemž zvažoval ještě nabídku z Realu Madrid. V Santosu měl smlouvu platnou do roku 2014, klub ale souhlasil s jeho předčasným odchodem.</p><br>

		<h3>FC Barcelona</h3>
		<p>V dresu FC Barcelona debutoval 30. července 2013 v přátelském utkání v PGE Areně v Gdaňsku proti domácímu týmu Lechia Gdańsk. Nastoupil 12 minut před koncem řádné hrací doby, střetnutí skončilo remízou 2:2. V La Lize debutoval 18. srpna 2013 v prvním kole proti Levante UD, do zápasu nastoupil v 63. minutě. Barcelona deklasovala soupeře 7:0. První gól vstřelil 21. srpna 2013 v Supercopa de España, kde vyrovnával na konečných 1:1 s Atléticem Madrid. S klubem tuto trofej vyhrál. 24. září 2013 vstřelil svůj první ligový gól (ve svém šestém zápase) proti Realu Valladolid, když v 5. minutě otevíral skóre utkání, které skončilo vítězstvím Barcelony 4:1. <br><br>

		S Barcelonou postoupil ze základní skupiny H do vyřazovacích bojů Ligy mistrů 2013/14, katalánský tým v ní obsadil první místo. V posledním utkání skupiny 11. prosince 2013 vstřelil hattrick proti skotskému týmu Celtic FC a přispěl tak k drtivému vítězství 6:1. Byl to jeho premiérový hattrick v dresu Barcelony a zároveň se poprvé střelecky prosadil v evropské Lize mistrů. O necelý týden později v 16. kole La Ligy sezóny 2013/14 zařídil dvěma góly výhru 2:1 nad Villarealem, dokázal tak opět střelecky nahradit zraněného klíčového hráče Barcelony Argentince Lionela Messiho.</p><br>
